// Build the static world-land SVG paths for the calendar WorldMap from
// Natural Earth 110m land (public domain). Equirectangular-projected into the
// 1000x500 viewBox used by src/features/calendar/WorldMap.tsx `project()`, so
// land + lat/lng markers share one coordinate space. Antarctica and sub-pixel
// dust are dropped. Output: src/data/worldLand.ts (committed static data —
// no runtime fetch, no map library). Re-run: `node scripts/build-worldmap.mjs`.
import { writeFileSync } from 'node:fs';

const SOURCE =
  'https://raw.githubusercontent.com/nvkelso/natural-earth-vector/master/geojson/ne_110m_land.geojson';

// Must match WorldMap.tsx project(lat, lng).
const projX = (lng) => ((lng + 180) / 360) * 1000;
const projY = (lat) => ((90 - lat) / 180) * 500;
const round = (n) => Math.round(n * 10) / 10;

function ringToSubpath(ring) {
  let d = '';
  for (let i = 0; i < ring.length; i++) {
    const [lng, lat] = ring[i];
    d += `${i === 0 ? 'M' : 'L'}${round(projX(lng))} ${round(projY(lat))}`;
    if (i < ring.length - 1) d += ' ';
  }
  return d + 'Z';
}

function ringAvgLat(ring) {
  let s = 0;
  for (const [, lat] of ring) s += lat;
  return s / ring.length;
}

function ringPxBox(ring) {
  let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity;
  for (const [lng, lat] of ring) {
    const x = projX(lng), y = projY(lat);
    if (x < minX) minX = x;
    if (y < minY) minY = y;
    if (x > maxX) maxX = x;
    if (y > maxY) maxY = y;
  }
  return Math.max(maxX - minX, maxY - minY);
}

const res = await fetch(SOURCE);
if (!res.ok) throw new Error(`fetch failed: HTTP ${res.status}`);
const geo = await res.json();

const paths = [];
let kept = 0;
let dropped = 0;
for (const feature of geo.features) {
  const g = feature.geometry;
  const polys =
    g.type === 'Polygon' ? [g.coordinates]
    : g.type === 'MultiPolygon' ? g.coordinates
    : [];
  for (const poly of polys) {
    const outer = poly[0];
    // Drop Antarctica (it dominates the bottom band and never hosts a round).
    if (ringAvgLat(outer) < -56) { dropped++; continue; }
    // Drop sub-pixel dust islands.
    if (ringPxBox(outer) < 1.6) { dropped++; continue; }
    // Outer ring + holes joined into one path; even-odd fill punches the holes.
    paths.push(poly.map(ringToSubpath).join(' '));
    kept++;
  }
}

const body = paths.map((p) => `  ${JSON.stringify(p)},`).join('\n');
const out = `// AUTO-GENERATED by scripts/build-worldmap.mjs — DO NOT EDIT BY HAND.
// Source: Natural Earth 110m land (public domain), equirectangular-projected
// into the 1000x500 viewBox shared with WorldMap.tsx project(lat, lng).
// Antarctica and sub-pixel islands removed. Re-run the script to regenerate.
export const WORLD_LAND_PATHS: readonly string[] = [
${body}
];
`;

writeFileSync(new URL('../src/data/worldLand.ts', import.meta.url), out);
console.log(`worldLand.ts written: ${kept} land paths kept, ${dropped} dropped, ${(out.length / 1024).toFixed(1)} KB`);
